package com.wljs.pojo;

public class Coordinates {

    private int minX;
    private int minY;
    private int maxX;
    private int maxY;
    private int totalX;
    private int totalY;

    public int getMinX() {
        return minX;
    }

    public void setMinX(int minX) {
        this.minX = minX;
    }

    public int getMinY() {
        return minY;
    }

    public void setMinY(int minY) {
        this.minY = minY;
    }

    public int getMaxX() {
        return maxX;
    }

    public void setMaxX(int maxX) {
        this.maxX = maxX;
    }

    public int getMaxY() {
        return maxY;
    }

    public void setMaxY(int maxY) {
        this.maxY = maxY;
    }

    public int getTotalX() {
        return totalX;
    }

    public void setTotalX(int totalX) {
        this.totalX = totalX;
    }

    public int getTotalY() {
        return totalY;
    }

    public void setTotalY(int totalY) {
        this.totalY = totalY;
    }
}
